function solution(my_string) {
    var answer = '';
    
    for (const letter of my_string) {
        switch (letter) {
            case 'a' :
                break;
            case 'e' :
                break;
            case 'i' :
                break;
            case 'o' :
                break;
            case 'u' :
                break;
            default :
                answer += letter;
        }
    }
    
    return answer;
}